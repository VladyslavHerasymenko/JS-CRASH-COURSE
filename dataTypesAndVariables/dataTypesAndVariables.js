/*
    Primitive date types
*/
// Boolean
const a = 1;
const b = 10;

console.log(a == b);
console.log(typeof (a == b));
console.log(a < b);
console.log(typeof (a < b));
console.log('--------------------------------------------------');

// Null

const c = null;

console.log(c);
console.log(typeof (c));
console.log('--------------------------------------------------');

// Undefined

let d;

console.log(d);
console.log(typeof (d));
console.log('--------------------------------------------------');

// Number

const f = 10;
const e = 0.011;
console.log(f);
console.log(typeof f);
console.log(e);
console.log(typeof e);
console.log('--------------------------------------------------');

// String

const str = 'Some text';

console.log(str);
console.log(typeof str);
console.log('--------------------------------------------------');
